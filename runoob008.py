"""
n=1: A ---> C
n=2: A ---> B, A ---> C, B ---> C
n=3: f(n-1, a, c, b), A ---> C, f(n-1, b, a, c)
"""
def f(n, a, b, c):
    if n == 1:
        print("{0} ---> {1}".format(a, c))
    else:
        f(n-1,a,c,b)
        print("{0} ---> {1}".format(a, c))
        f(n-1,b,a,c)
f(1,"A","B","C")
print("-------------------------------------")
f(2,"A","B","C")
print("-------------------------------------")
f(5,"A","B","C")
print("-------------------------------------")
print("-------------------------------------")
print("-------------------------------------")
